numbers = [1,12,3,523,52]
# sort() 오름차순

numbers.sort() # 오름차순으로 정렬
print(numbers)

#reverse() 요소를 역순으로
numbers.reverse()
print(numbers)

numbers = [1,12,3,523,52]
numbers.reverse()
print(numbers)

# 내림차순으로 하고 싶으면 sort(reverse = True)
numbers = [1,12,3,523,52]
numbers.sort(reverse=True)
print(numbers)

